
public class Fundamentals_ControlFlow{
	public static void main(String[] args) {
		comparisonOperators();
    }

	public static void comparisonOperators() {
		int x = 1;
		int y = 1;
		System.out.println(x == y); //prints true = equal
		System.out.println(x != y); //prints false = not equal
		System.out.println(x <= y); //prints false = not equal
		// >, >=, <, <=, 
		
	}
}